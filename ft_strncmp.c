#include "libft.h"

int	ft_strncmp(const char *s1, const char *s2, size_t n)
{
	size_t	i;

	i = 0;
	while (s1[i] && s2[i] && (t_byte)s1[i] == (t_byte)s2[i] && i < n)
		i++;
	return ((t_byte)s1[i] - (t_byte)s2[i]);
}
